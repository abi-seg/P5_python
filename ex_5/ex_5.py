def sum(a, b):

    """"
    Function retourne la somme de deux nombres.
    
    Paramètres:
       a (int or float): Le premier nombre
       b (int or foat): Le second nombre
    
    Retourne:
      int ou float : La somme de a et b.

    """
    return a + b


 
def subtraction(a, b):
    """"
    Function retourne le différence entre deux nombres.

    Paramètres:
        a (int or float): Le premier nombre
        b (int or foat): Le second nombre
    
    Retourne:
      int ou float : Le résultat de a moins b.

    """  
    return a - b
